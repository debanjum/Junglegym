Welcome! This is the BITS Energy Monitoring Website.<br /><br />

This site contains the load profile, that is the power consumption patterns, of all the hostels of the BITS Pilani, K.K Birla Goa Campus.
The data is based on real-time load sensing by a sensor network set up across the 14 hostels on campus.<br /><br />

You can view the data by going to the "Readings" page of the website.
On selecting the hostel name and the view type you will be presented with the load profile graph. To zoom in on a dataset select the desired data area on the graph using the mouse. Double-clicking will revert the graph back to its original view.<br /><br />

